using System;

namespace ConsoleCreatures
{
  class Program
  {
    static void Main()
    {
      // These commands print the individual Strings in their parenthesis to the console to create the image of a Ghost named Willy.
      Console.WriteLine("      .-."); 
      Console.WriteLine("     (o o)");
      Console.WriteLine("     | O |");
      Console.WriteLine("     |   |");
      Console.WriteLine("     '~~~'");
      Console.WriteLine("Willy the Ghost");
     }
  }
}